    public void readJtable() {
        DefaultTableModel modelo = (DefaultTableModel) txtTabela.getModel();
        ConsultaDAO consulta = new ConsultaDAO();
         for(LivroBeans e: consulta.read()){
            
            modelo.addRow(new Object[]{
                
                e.getId(),
                e.getTitulo(),
                e.getAutor(),
                e.getGenero(),
                e.getQtd()
                
            });
            
            
        }

    }

