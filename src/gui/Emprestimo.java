    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTemprestimos.getModel();

        BibliotecaDAO emprestimo = new BibliotecaDAO();
        
        for(EmprestimoBeans e: emprestimo.read()){
            
            modelo.addRow(new Object[]{
                
                e.getId(),
                e.getId_aluno(),
                e.getId_livro(),
                e.getDataP(),
                e.getDataD()
                
            });
            
            
        }
        
    }
    
    
